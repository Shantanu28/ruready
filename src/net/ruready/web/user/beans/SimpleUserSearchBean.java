package net.ruready.web.user.beans;

import java.io.Serializable;

public class SimpleUserSearchBean implements Serializable
{
	private static final long serialVersionUID = 8504285614385392239L;
	
	private String searchString = "";
	private Boolean schoolFilter = false;
	private ResultsPagingBean pagingBean = new ResultsPagingBean(0);
	
	public String getSearchString()
	{
		return searchString;
	}
	
	public void setSearchString(final String searchString)
	{
		this.searchString = searchString;
	}
	
	public Boolean getSchoolFilter()
	{
		return schoolFilter;
	}
	
	public void setSchoolFilter(final Boolean schoolFilter)
	{
		this.schoolFilter = schoolFilter;
	}
	
	public ResultsPagingBean getPagingBean()
	{
		return pagingBean;
	}
	
	public void setPagingBean(final ResultsPagingBean pagingBean)
	{
		this.pagingBean = pagingBean;
	}
}
